from ultralytics import YOLO

def main():
    # 1. Загрузка базовой предобученной модели (Transfer Learning)
    # Мы берем "nano" версию, так как она обучится быстрее всего и будет быстро работать
    model = YOLO("yolov8n.pt") 

    print("Начинаем процесс обучения...")

    # 2. Запуск процесса дообучения
    # ВНИМАНИЕ: Если у вас на компьютере нет видеокарты NVIDIA, 
    # этот процесс может быть очень долгим. Для быстрой работы 
    # запустите этот код в Google Colab, предварительно загрузив туда датасет.
    
    results = model.train(
        data="data.yaml",   # Файл конфигурации (где лежат фото и как называются классы)
        epochs=20,          # Сколько циклов пройдено по всем картинкам датасета
        imgsz=640,          # Размер, к которому будут сжаты ваши загруженные картинки
        batch=16,           # Размер пакета (сколько картинок загружается в видеокарту за 1 шаг)
        name="my_model"     # Название папки, куда сохранятся результаты
    )

    # 3. Сохранение (извлечение) обученной модели
    # Библиотека ultralytics СОХРАНЯЕТ модель АВТОМАТИЧЕСКИ после каждой эпохи.
    # Итоговый лучший файл "best.pt" будет спрятан глубоко в папках: runs/detect/my_model/weights/
    
    # Для удобства, давайте скопируем её оттуда прямо в нашу текущую папку:
    import shutil
    import os
    
    saved_model_path = "runs/detect/my_model/weights/best.pt"
    export_path = "my_custom_model.pt"
    
    if os.path.exists(saved_model_path):
        shutil.copy(saved_model_path, export_path)
        print(f"\nОбучение завершено! Модель успешно сохранена как: {export_path}")
    else:
        print("\nПроизошла ошибка, файл модели не найден.")

if __name__ == '__main__':
    main()
