import cv2  # Основная библиотека OpenCV
import numpy as np # Библиотека для работы с матрицами
import matplotlib.pyplot as plt # Для отображения графиков и картинок

# --- 1. ЗАГРУЗКА ИЗОБРАЖЕНИЯ ---
# Важно: OpenCV загружает изображения в формате BGR (Blue, Green, Red),
# в то время как мы привыкли к RGB.
image_path = 'sample.jpg' # Укажите здесь имя вашего файла
img = cv2.imread(image_path)

# Проверка, загрузилась ли картинка (частая ошибка новичков - неверный путь)
if img is None:
    print("Ошибка: Не удалось загрузить изображение. Проверьте путь к файлу.")
else:
    print("Изображение успешно загружено!")

# --- 2. АНАЛИЗ СТРУКТУРЫ ---
# Изображение в компьютере - это просто трехмерная матрица чисел (NumPy array)
print(f"Тип данных: {type(img)}")
print(f"Размерность (Height, Width, Channels): {img.shape}")
print(f"Количество пикселей: {img.size}")
print(f"Тип данных пикселя (обычно uint8): {img.dtype}")

# --- 3. ОТОБРАЖЕНИЕ (НЮАНС С ЦВЕТАМИ) ---
# Если просто вывести BGR картинку через matplotlib, она будет синей (цвета перепутаны)
# Давайте сконвертируем её в RGB для корректного отображения
img_rgb = cv2.cvtColor(img, cv2.COLOR_BGR2RGB)

plt.figure(figsize=(10, 5))

# Показываем оригинал (как видит OpenCV) - цвета будут искажены
plt.subplot(1, 2, 1)
plt.imshow(img)
plt.title("Оригинал (BGR) - Ошибка цветов")
plt.axis('off')

# Показываем корректный вид (RGB)
plt.subplot(1, 2, 2)
plt.imshow(img_rgb)
plt.title("Конвертация в RGB")
plt.axis('off')
plt.show()

# --- 4. ДОСТУП К ПИКСЕЛЯМ ---
# Координаты в OpenCV: (Y, X) -> (Строка, Столбец).
# Начало координат (0,0) - левый ВЕРХНИЙ угол.
y, x = 50, 100 # Координаты пикселя
pixel_value = img[y, x]
print(f"Значение пикселя в точке (y={y}, x={x}): {pixel_value}") # Выведет [B, G, R]

# Изменим цвет пикселя (сделаем его ярко-зеленым)
# Внимание: изменяем в исходном BGR изображении
# Green в BGR это [0, 255, 0]
img[y, x] = [0, 255, 0]

# --- 5. ВЫРЕЗАНИЕ ОБЛАСТИ (ROI - Region of Interest) ---
# Используем срезы NumPy (Slicing): [y_start:y_end, x_start:x_end]
# Вырежем квадрат 100x100 пикселей
crop_img = img_rgb[100:200, 150:250]

plt.imshow(crop_img)
plt.title("ROI (Вырезанный фрагмент)")
plt.axis('off')
plt.show()

# --- 6. РИСОВАНИЕ ПРИМИТИВОВ ---
# Нарисуем прямоугольник прямо на картинке
# Аргументы: изображение, левый-верхний угол, правый-нижний, цвет (R,G,B), толщина
output_img = img_rgb.copy() # Создаем копию, чтобы не портить оригинал
cv2.rectangle(output_img, (150, 100), (250, 200), (255, 0, 0), 2) # Красный прямоугольник

plt.imshow(output_img)
plt.title("Рисование прямоугольника")
plt.axis('off')
plt.show()

# --- 7. СОХРАНЕНИЕ РЕЗУЛЬТАТА ---
# OpenCV сохраняет в BGR, поэтому если мы сохраняем img_rgb, цвета перепутаются снова.
# Нужно либо конвертировать обратно в BGR, либо сохранять исходный `img`.
# Сохраним вырезанный кусочек. Сначала вернем его в BGR для корректной записи.
crop_bgr = cv2.cvtColor(crop_img, cv2.COLOR_RGB2BGR)
cv2.imwrite('my_crop.jpg', crop_bgr)
print("Файл my_crop.jpg сохранен!")